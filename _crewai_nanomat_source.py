    "# 2. Cargas útiles binarias rápidas: serializa usando Protocol Buffers en lugar de JSON,\n",
    "#    lo que reduce el tamaño de payload y el tiempo de parseo.\n",
    "# 3. Streams bidireccionales: perfecto para agentes que envían actualizaciones en streaming.\n",
    "# 4. Tipado fuerte: los contratos definidos en archivos .proto previenen errores de estructura.\n",
