    "\n",
    "class RateLimiter:\n",
    "    \"\"\"\n",
    "    Token bucket rate limiter thread-safe.\n",
    "    Permite max_calls por min_period segundos.\n",
    "    \"\"\"\n",
    "    def __init__(self, max_calls: int, min_period: float = 60.0):\n",
    "        self.max_calls = max_calls\n",
    "        self.min_period = min_period\n",
    "        self.calls = []\n",
    "        self._lock = threading.Lock()\n",
    "    \n",
    "    def __call__(self, func):\n",
    "        \"\"\"Decorador: aplica rate limiting a la función.\"\"\"\n",
    "        def wrapper(*args, **kwargs):\n",
    "            with self._lock:\n",
    "                now = time.time()\n",
    "                # Eliminar llamadas fuera de la ventana temporal\n",
    "                self.calls = [t for t in self.calls if now - t < self.min_period]\n",
    "                \n",
    "                if len(self.calls) >= self.max_calls:\n",
    "                    wait_time = self.min_period - (now - self.calls[0])\n",
    "                    raise RuntimeError(\n",
    "                        f'Rate limit: {self.max_calls} calls/{self.min_period}s. '\n",
    "                        f'Esperar {wait_time:.1f}s'\n",
    "                    )\n",
    "                \n",
    "                self.calls.append(now)\n",
    "            return func(*args, **kwargs)\n",
    "        return wrapper\n",
