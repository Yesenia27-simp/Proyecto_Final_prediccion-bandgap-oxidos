    "import time\n",
    "\n",
    "def rest_call_latency():\n",
    "    # Simula latencia de HTTP/1.1 (JSON serialization/deserialization, TCP handshake)\n",
    "    time.sleep(0.012)\n",
    "    return True\n",
    "\n",
