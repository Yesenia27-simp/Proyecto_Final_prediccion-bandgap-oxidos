    "# Espera: 1s → 2s → 4s entre intentos\n",
    "# Solo reintenta en errores de red o rate limit (429)\n",
    "@retry(\n",
    "    stop=stop_after_attempt(3),\n",
    "    wait=wait_exponential(multiplier=1, min=1, max=10),\n",
    "    retry=retry_if_exception_type((httpx.TransportError, httpx.TimeoutException)),\n",
    "    reraise=True  # Si falla 3 veces, propaga la excepción original\n",
    ")\n",
    "def fetch_with_retry(url: str, params: dict = None, headers: dict = None) -> dict:\n",
    "    \"\"\"Hace un GET HTTP con retry automático en errores transitorios.\"\"\"\n",
    "    with httpx.Client(timeout=10, follow_redirects=True) as client:\n",
    "        response = client.get(url, params=params, headers=headers)\n",
    "        \n",
    "        # Rate limit → esperar y reintentar manualmente no es necesario con tenacity,\n",
    "        # pero podemos loguear el evento\n",
    "        if response.status_code == 429:\n",
    "            retry_after = int(response.headers.get('Retry-After', '5'))\n",
    "            raise httpx.HTTPStatusError(\n",
    "                f'Rate limited. Retry after {retry_after}s',\n",
    "                request=response.request,\n",
    "                response=response\n",
    "            )\n",
    "        \n",
    "        response.raise_for_status()\n",
    "        return response.json()\n",
