    "<a id='4-rag'></a>\n",
    "## 4. Arquitecturas RAG\n",
    "\n",
    "### 4.1 Naive RAG vs Advanced RAG vs Agentic RAG\n",
    "\n",
    "RAG (Retrieval-Augmented Generation) es el patrón de memoria semántica más usado en producción. En esencia: en lugar de hacer fine-tuning del modelo con datos propios, se recuperan los datos relevantes en tiempo de inferencia y se inyectan en el contexto.\n",
    "\n",
    "Pero no todo RAG es igual. Existe una progresión de complejidad con trade-offs claros:\n",
    "\n",
    "```\n",
    "NAIVE RAG (2022-2023)\n",
    "  Query → embed → k-NN → chunks → LLM\n",
    "  Problema: chunks irrelevantes degrades la respuesta\n",
    "  Problema: sin feedback loop — no aprende de malos resultados\n",
    "\n",
    "ADVANCED RAG (2023-2024)\n",
    "  Query → rewrite → embed → k-NN → rerank → filter → LLM\n",
    "  Mejora: query rewriting mejora la recuperación\n",
    "  Mejora: reranking filtra falsos positivos semánticos\n",
    "  Problema: pipeline fijo — no puede decidir cuándo recuperar\n",
    "\n",
    "AGENTIC RAG (2024-2026) ← lo que usamos en esta notebook\n",
    "  El agente decide:\n",
    "    - Si necesita recuperar o ya sabe la respuesta\n",
    "    - Qué query enviar al retriever (puede reformularla)\n",
    "    - Si el resultado es suficiente o necesita más iteraciones\n",
    "    - De qué fuentes recuperar (múltiples vector stores)\n",
    "  Implementación: LangGraph nodo de retrieval + condición de suficiencia\n",
    "```\n",
    "\n",
    "**¿Cuándo usar cada uno?**\n",
    "\n",
    "| Escenario | RAG recomendado |\n",
    "|-----------|----------------|\n",
    "| FAQ estático, bajo tráfico | Naive RAG |\n",
    "| Documentos técnicos con terminología específica | Advanced RAG + reranking |\n",
    "| Sistema que necesita decidir cuándo buscar | Agentic RAG |\n",
    "| Corpus masivo (>100k docs) con preguntas complejas | GraphRAG (ver U5_06) |\n",
    "\n",
    "### 4.2 Estrategias de Chunking\n",
    "\n",
    "El chunking es el paso más subestimado de RAG. La calidad del chunking determina en gran medida la calidad de la recuperación, más que el modelo de embeddings.\n",
    "\n",
    "**El problema de chunk_size fijo:** Un chunk de 500 tokens que corta en medio de un párrafo técnico pierde el contexto. Un chunk demasiado grande incluye información irrelevante que confunde al reranker.\n",
    "\n",
    "Estrategias en orden de sofisticación:\n",
    "\n",
    "1. **Fixed-size**: `RecursiveCharacterTextSplitter(chunk_size=500)` — rápido, funciona para texto homogéneo\n",
    "2. **Recursive con separadores**: respeta párrafos, oraciones, palabras en ese orden — mejor para prosa técnica\n",
    "3. **Semantic chunking**: agrupa oraciones por similitud semántica — mejor para documentos con secciones heterogéneas\n",
    "4. **Agentic chunking**: el LLM decide los límites del chunk — máxima calidad, máximo costo\n",
    "\n",
    "### 4.3 Embeddings y Modelos de Representación\n",
    "\n",
    "Los embeddings convierten texto en vectores densos. La elección del modelo afecta directamente la calidad del retrieval.\n",
    "\n",
    "| Modelo | Dimensiones | Multilingüe | Costo | Mejor para |\n",
    "|--------|-------------|-------------|-------|------------|\n",
    "| `text-embedding-3-large` (OpenAI) | 3072 | Sí | API | Inglés técnico, alto rendimiento |\n",
    "| `text-embedding-3-small` (OpenAI) | 1536 | Sí | Bajo | Producción, balance costo/calidad |\n",
    "| `BGE-M3` (BAAI) | 1024 | Sí (100+ idiomas) | Local | Multilingüe, español |\n",
    "| `nomic-embed-text` (Nomic) | 768 | No | Local/API | Documentos largos, contexto 8192 tokens |\n",
    "| `sentence-transformers/all-MiniLM-L6-v2` | 384 | No | Local, rápido | Prototipado, bajo costo computacional |\n",
    "\n",
    "### 4.4 Reranking: Por Qué un Solo Retriever No Es Suficiente\n",
    "\n",
    "El retrieval con embeddings (k-NN) usa **similitud semántica aproximada**. El reranker usa un **cross-encoder** más lento pero preciso que evalúa el par (query, chunk) completo.\n",
    "\n",
    "```\n",
    "k-NN retrieval → top-50 candidatos (rápido, ~ms)\n",
    "      ↓\n",
    "cross-encoder reranker → top-5 mejores (lento, ~200ms, pero mucho más preciso)\n",
    "      ↓\n",
    "LLM con los 5 mejores chunks\n",
    "```\n",
    "\n",
    "El reranking mejora la precisión entre 15-40% en benchmarks de QA comparado con k-NN solo (Nogueira et al., 2020; Cohere Rerank benchmarks, 2024).\n",
