    "---\n",
    "<a id='3-taxonomia'></a>\n",
    "## 3. Taxonomía Formal de Memoria en Agentes\n",
    "\n",
    "### 3.1 Los 4 Tipos de Memoria\n",
    "\n",
    "La memoria en sistemas de IA es uno de los temas más malentendidos del campo. Cuando los desarrolladores hablan de \"darle memoria a un agente\", a menudo se refieren a una sola cosa: pasar los mensajes anteriores en el contexto. Pero eso es solo uno de cuatro tipos de memoria fundamentalmente distintos, cada uno con características, tecnologías de implementación, y propósitos diferentes.\n",
    "\n",
    "Esta taxonomía proviene de la convergencia de tres fuentes: la literatura cognitiva sobre memoria humana (Tulving, 1985), el framework MemGPT/Letta (Packer et al., 2023), y los patrones de producción de Mem0 y Zep (2024-2026).\n",
    "\n",
    "| Tipo | ¿Qué almacena? | Duración | Implementación | Analogía humana |\n",
    "|------|----------------|----------|----------------|-----------------|\n",
    "| **Sensorial / In-Context** | El historial activo de la conversación actual, contexto inyectado en el prompt | Solo mientras el token está en la ventana | `ConversationBufferMemory`, mensajes en el `state` de LangGraph | Memoria de trabajo (RAM) |\n",
    "| **Episódica** | Eventos y experiencias pasadas: \"la sesión del martes\", \"el usuario pidió X hace 3 días\" | Persistente entre sesiones | Mem0, Zep, tablas de episodios en PostgreSQL, MemGPT/Letta | Memoria autobiográfica |\n",
    "| **Semántica** | Conocimiento factual y conceptual: documentos, FAQs, bases de conocimiento | Persistente, actualizable | ChromaDB, Pinecone, Weaviate, pgvector + RAG | Memoria declarativa / conocimiento general |\n",
    "| **Procedimental** | Cómo hacer cosas: skills, herramientas, estrategias de razonamiento | Persistente, versionada | Skill Registry, few-shot examples, fine-tuning, LoRA | Memoria muscular / hábitos |\n",
    "\n",
    "#### ¿Por qué importa esta distinción?\n",
    "\n",
    "Un agente que solo tiene memoria **sensorial** (buffer del historial) olvida todo al cerrar la sesión. Un agente con memoria **episódica** recuerda las preferencias del usuario de la semana pasada. Un agente con memoria **semántica** puede responder preguntas sobre un documento que nunca estuvo en su contexto. Un agente con memoria **procedimental** puede \"aprender\" nuevas capacidades sin fine-tuning.\n",
    "\n",
    "La diferencia entre un chatbot básico y un asistente persistente productivo es exactamente cuántos de estos tipos implementa.\n",
    "\n",
    "### 3.2 Coordinación de los 4 Tipos en un Agente\n",
    "\n",
    "El patrón de coordinación más efectivo es el **warm-up secuencial**: antes de responder a la primera consulta de una sesión, el agente carga activamente los tipos de memoria que necesita:\n",
    "\n",
    "```\n",
    "INICIO DE SESIÓN\n",
    "     ↓\n",
    "1. [Procedimental] Cargar skills del registry → define qué PUEDE hacer el agente\n",
    "2. [Episódica]     Recuperar episodios pasados relevantes → define lo que RECUERDA\n",
    "3. [Semántica]     Inicializar vector store listo para queries → define lo que SABE\n",
    "4. [Sensorial]     Contexto activo vacío, listo para recibir la primera consulta\n",
    "     ↓\n",
    "PRIMERA CONSULTA DEL USUARIO\n",
    "     ↓\n",
    "[Semántica] RAG retrieval → chunks relevantes al query\n",
    "[Episódica] episodic_retriever → episodios pasados relevantes al query\n",
    "[Sensorial] Historial de la sesión actual\n",
    "[Procedimental] Skill apropiada según la tarea (routing)\n",
    "     ↓\n",
    "LLM con contexto enriquecido → RESPUESTA\n",
    "     ↓\n",
    "FIN DE SESIÓN → guardar nuevo episodio en memoria episódica\n",
    "```\n",
    "\n",
    "### 3.3 La Analogía del Ser Humano\n",
    "\n",
    "Para anclar estos conceptos en intuición:\n",
    "\n",
    "- **Sensorial**: Lo que tienes en la mente ahora mismo leyendo esta oración. Cuando termines de leer este párrafo, los detalles exactos se difuminarán.\n",
    "- **Episódica**: Recuerdas lo que desayunaste el lunes pasado, quién te lo preparó, y la conversación que tuviste. Es autobiográfica, tiene contexto temporal.\n",
    "- **Semántica**: Sabes que el agua hierve a 100°C a nivel del mar. No recuerdas dónde aprendiste eso — simplemente lo sabes. Es conocimiento descontextualizado.\n",
    "- **Procedimental**: Sabes montar en bicicleta. No puedes explicar exactamente qué músculos activas — lo haces automáticamente. Las skills del agente son esto: capacidades codificadas que se activan.\n",
    "\n",
    "La diferencia entre memoria semántica y episódica es sutil pero crucial en ingeniería: la episódica tiene **quién, cuándo, dónde** (metadatos temporales y contextuales); la semántica solo tiene **qué** (contenido factual)."
