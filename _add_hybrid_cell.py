    "# Ejercicio 5: Budget Guard en LangGraph\n",
    "# ============================================================\n",
    "from typing import TypedDict\n",
    "from langgraph.graph import StateGraph, END\n",
    "\n",
    "# 1. Definir Estado del Grafo\n",
    "class GraphState(TypedDict):\n",
    "    input: str\n",
    "    output: str\n",
    "    budget_guard: BudgetGuard\n",
    "    circuit_open: bool\n",
    "\n",
    "# 2. Nodo de validación de presupuesto\n",
    "def check_budget(state: GraphState):\n",
    "    guard = state['budget_guard']\n",
    "    status = guard.status()\n",
    "    print(f'[check_budget] Gasto acumulado: ${status[\"cost_usd\"]} | Presupuesto: ${guard.budget_usd}')\n",
    "    return {'circuit_open': status['circuit_open']}\n",
    "\n",
    "# 3. Nodo de ejecución (simula llamada cara de LLM)\n",
    "def execute_node(state: GraphState):\n",
    "    guard = state['budget_guard']\n",
    "    # Simular una llamada con muchos tokens para activar el budget limit ($0.06)\n",
    "    # gemini costs: input $0.10/M, output $0.40/M. Una llamada de 300,000 in y 100,000 out es $0.07\n",
    "    guard.record_call(300000, 100000, desc='Simulación LLM LangGraph')\n",
    "    print('[execute] Llamada de LLM completada con éxito.')\n",
    "    return {'output': 'Investigación de nanotecnología generada con éxito.'}\n",
    "\n",
    "# 4. Nodo de salida elegante por falta de presupuesto\n",
    "def graceful_exit(state: GraphState):\n",
    "    print('[graceful_exit] Presupuesto excedido. Terminando de forma controlada.')\n",
    "    return {'output': 'Acceso bloqueado: Presupuesto de tokens de API superado. Por favor, reabastece el budget.'}\n",
    "\n",
    "# 5. Arista condicional de ruteo\n",
    "def router(state: GraphState):\n",
    "    if state.get('circuit_open', False):\n",
    "        return 'graceful_exit'\n",
    "    return 'execute'\n",
    "\n",
    "# 6. Construcción del Grafo\n",
    "workflow = StateGraph(GraphState)\n",
    "workflow.add_node('check_budget', check_budget)\n",
    "workflow.add_node('execute', execute_node)\n",
    "workflow.add_node('graceful_exit', graceful_exit)\n",
    "\n",
