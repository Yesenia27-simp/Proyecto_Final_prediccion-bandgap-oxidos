    "## 1. ¿Por Qué Grafos? Limitaciones de los Vector Stores <a id='por-que-grafos'></a>\n",
    "\n",
    "### El problema central: las relaciones\n",
    "\n",
    "Un vector store almacena textos como vectores en un espacio n-dimensional. Es excelente para preguntas del tipo:\n",
    "\n",
    "> *\"¿Qué papers hablan sobre transformers?\"* → recuperación por similitud semántica\n",
    "\n",
    "Pero falla ante preguntas que requieren **razonar sobre relaciones**:\n",
    "\n",
    "> *\"¿Qué investigadores han colaborado con colegas que trabajan en el mismo lab que el autor de este paper?\"*\n",
    "\n",
    "Esta es una consulta **multi-hop**: requiere seguir relaciones (autor → paper → lab → colegas → colaboraciones). Los vector stores no codifican relaciones — solo codifican contenido.\n",
    "\n",
    "### Cuándo usar cada tecnología\n",
    "\n",
    "| Pregunta | Tecnología óptima | Razón |\n",
    "|----------|-------------------|-------|\n",
    "| Similitud semántica entre textos | Vector store (ChromaDB, Pinecone) | Embeddings capturan significado |\n",
    "| Relaciones entre entidades | Knowledge graph (Neo4j, Kuzu) | Estructura nativa de grafos |\n",
    "| Caminos entre nodos | Knowledge graph + Cypher | Path queries nativas |\n",
    "| Contexto de conversación | Graphiti / episodic memory | Episodios en grafo temporal |\n",
    "| \"¿Qué es similar a X?\" | Vector store | Búsqueda por vecinos |\n",
    "| \"¿Quién conoce a quién a través de quién?\" | Knowledge graph | Multi-hop queries |\n",
    "| Las dos preguntas en el mismo sistema | Graph RAG híbrido | Combinar ambas |\n",
    "\n",
    "### Taxonomía de grafos que usaremos\n",
    "\n",
    "```\n",
    "NetworkX  →  simple, en memoria, sin persistencia, ideal para prototipos\n",
    "    ↓\n",
    "Kuzu      →  embebido (como SQLite), persistente, sin servidor, Cypher nativo\n",
    "    ↓\n",
    "Neo4j     →  servidor, producción, ACID, Cypher avanzado, visualización\n",
    "    ↓\n",
    "GraphRAG  →  capa sobre el grafo orientada a LLMs\n",
    "    ↓\n",
    "Graphiti  →  memoria episódica en grafo para agentes multi-turno\n",
    "```"
